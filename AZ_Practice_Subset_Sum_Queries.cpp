#define ll int64_t
#define pb push_back
int N, Q, n;

vector<vector<int>> fin;
void f(vector<int> &arr, int x, int i, vector<int> &temp, int idx)
{
    if(i == n)
    {
        if(x == 0)
        {
            vector<int> t = temp;
            fin.pb(t);
            return;
        }
        else return;
    }

    //transitions

    if(fin.size() < idx+1)
    f(arr,x,i+1,temp,idx);

    if(fin.size() < idx+1 and x-arr[i] >= 0)
    { 
        temp.pb(i);
        f(arr,x-arr[i], i+1,temp,idx);
        temp.erase(temp.end()-1);
    }
    

}

vector<vector<int>> subset_queries(vector<int> &arr, vector<int> &queries) {
    // Write code here.
    for(int i = 0; i < Q; ++i)
    {   
        vector<int> temp;
        f(arr,queries[i],0,temp,i);
        if(fin.size() < i+1)
        {
            vector<int> t = {-1};
            fin.pb(t);
        }
    }
    return fin;
}


void solve() {
    
    cin >> N >> Q;
    n = N;
    vector<int> arr(N);
    for (int i = 0; i < N; i++)cin >> arr[i];
    vector<int> queries(Q);
    for (int i = 0; i < Q; i++)cin >> queries[i];
    auto ans = subset_queries(arr, queries);

    // checker.
    if (ans.size() != Q) {
        cout << 101 << endl;
        return;
    }
    for (int i = 0; i < Q; i++) {
        auto x = ans[i];
        if (x.size() == 0) {
            cout << 101 << endl;
            continue;
        }
        if (x.size() == 1 && x[0] == -1) {
            cout << -1 << endl;
            continue;
        }
        int sum = 0, p = -10;
        for (auto y : x) {
            if (y < 0 || y >= N || p >= y ) { // valid 0-indexed.
                sum = -1111;
                break;
            }
            p = y;
            sum += arr[y];
        }
        if (sum == queries[i]) {
            cout << 1 << endl;
        }
        else cout << 101 << endl;
    }
    debug_out(fin);
}
